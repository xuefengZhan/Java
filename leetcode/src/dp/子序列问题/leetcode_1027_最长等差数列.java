    public int longestArithSeqLength(int[] nums) {
        int n = nums.length;

        int res = 0;

        HashMap<Integer,Integer>[] map = new HashMap[n];
        //初始化
        Arrays.fill(map,new HashMap<>());

        for(int i = 0;i<n;i++){
            for(int j = 0;j<i;j++){

                int diff = nums[i] - nums[j];

                if(!map[j].containsKey(diff)){
                    map[i].put(diff,1);
                }else{
                    map[i].put(diff,Math.max(map[j].get(diff) + 1, map[i].getOrDefault(diff,0)));
                }

                res = Math.max( map[i].get(diff) + 1,res) ;
            }
        }
        return res;
    }


